var path = require('path');
var fs = require('fs');
var util = require('util');
var hfc = require('fabric-client');
var helper = require('./helper.js');
var logger = helper.getLogger('Query');

var queryChaincode = async function(peer, channelName, chaincodeName, args, fcn, username, org_name) {
	try {
		// first setup the client for this org
		var client = await helper.getClientForOrg(org_name, username);
		logger.debug('Successfully got the fabric client for the organization "%s"', org_name);
		var channel = client.getChannel(channelName);
		if(!channel) {
			let message = util.format('Channel %s was not defined in the connection profile', channelName);
			logger.error(message);
			throw new Error(message);
		}
		let txID = client.newTransactionID();
		let target = helper.newPeers([peer], org_name)[0];


		// send query
		var request = {
			targets: [target],
			chaincodeId: chaincodeName,
			fcn: fcn,
			args: args,
			txId: txID
		};
		let response_payloads = await channel.queryByChaincode(request);

		if (response_payloads) {
			logger.info(response_payloads);
			let payload = response_payloads[0].toString('utf8');
			logger.info(payload);
			return payload;
		} else {
			logger.error('response_payloads is null');
			return 'response_payloads is null';
		}
	} catch(error) {
		logger.error('Failed to query due to error: ' + (error.stack || error));
		throw error;
	}
};
